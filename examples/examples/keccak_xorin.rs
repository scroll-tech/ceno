fn main() {
    for (case, &len) in LENGTHS.iter().enumerate() {
        let input = (0..len)
            .map(|i| (i as u8).wrapping_mul(37).wrapping_add(11))
            .collect::<Vec<_>>();

        assert_eq!(digest_one_shot(&input), DIGESTS[case]);
        assert_eq!(digest_fragmented(&input), DIGESTS[case]);
    }
}

fn digest_one_shot(input: &[u8]) -> [u8; 32] {
    let mut hasher = Keccak::v256();
    hasher.update(input);
    let mut digest = [0u8; 32];
    hasher.finalize(&mut digest);
    digest
}

fn digest_fragmented(input: &[u8]) -> [u8; 32] {
    const CHUNKS: [usize; 6] = [1, 3, 17, 64, 5, 89];
    let mut hasher = Keccak::v256();
    let mut offset = 0;
    let mut chunk_index = 0;
    while offset < input.len() {
        let end = (offset + CHUNKS[chunk_index % CHUNKS.len()]).min(input.len());
        hasher.update(&input[offset..end]);
        offset = end;
        chunk_index += 1;
    }
    let mut digest = [0u8; 32];
    hasher.finalize(&mut digest);
    digest
}
